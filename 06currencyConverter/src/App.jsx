function App() {
    const [amount, setAmount] = useState(0);
    const [from, setFrom] = useState("USD");
    const [to, setTo] = useState("PKR");
    const [convertedAmount, setConvertedAmount] = useState(0);
  
    const { data: currencyInfo, error } = useCurrencyInfo(from);
  
    
    
    const options = Object.keys(countryList)
  
    const swap = () => {
      setFrom(to);
      setTo(from);
      setConvertedAmount(amount);
      setAmount(convertedAmount);
    };
  
    const convert = () => {
        if (currencyInfo[to]) {
          setConvertedAmount(amount * currencyInfo[to]); // Multiply by the conversion rate
        } else {
          alert("Currency data not available.");
        }
      };
      
    if (error) {
      return <div>Error: {error}</div>;
    }
  
    return (
        
      <div
        className="w-full h-screen flex flex-wrap justify-center items-center bg-cover bg-no-repeat"
        style={{
          backgroundImage: `url('https://media.istockphoto.com/id/2124792712/photo/dollars-connecting-in-plexus.webp?a=1&b=1&s=612x612&w=0&k=20&c=HM-By1CLsq-G9M3ou0PrRPZU5m3WxstpF4VDgNuWjSc=')`,
        }}
      > 
        
        <div className="w-full">
          <div className="w-full max-w-md mx-auto border border-gray-60 rounded-lg p-5 backdrop-blur-sm bg-white/30">
            {/* <div>SUdais khan</div> */}
            <form
              onSubmit={(e) => {
                e.preventDefault();
                convert();
              }}
              
            >
              <div className="w-full mb-1">
                <InputBox
                  label="From"
                  amount={amount}
                  currencyOptions={options}
                  onCurrencyChange={(currency) => setFrom(currency)}
                  selectCurrency={from}
                  onAmountChange={(amount) => setAmount(amount)}
                />
              </div>
              <div className="relative w-full  h-0.5">
                <button
                  type="button"
                  className="absolute left-1/2 -translate-x-1/2 -translate-y-1/2 border-2 border-white rounded-md bg-blue-600 duration-200 hover:bg-blue-900 text-white px-4 py-0.5"
                  onClick={swap}
                >
                  swap
                </button>
              </div>
              <div className="w-full mt-1 mb-4">
                <InputBox
                  label="To" 
                  amount={convertedAmount}
                  onCurrencyChange={(currency) => setTo(currency)}
                  currencyOptions={options}
                  selectCurrency={to}
                  amountDisable
                />
              </div>
              <button type="submit" className="w-full bg-blue-600 text-white px-4 py-3 hover:bg-blue-900 duration-200 rounded-lg">
                Convert {from.toUpperCase()} to {to.toUpperCase()}
              </button>
            </form>
          </div>
        </div>
      </div>
    );
  }
  
  export default App;
  
